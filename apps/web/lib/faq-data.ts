export type FaqCategory = "gameplay" | "account" | "tech" | "privacy";

export interface FaqAnswerPart {
  text: string;
  href?: string;
}

export interface FaqItem {
  category: FaqCategory;
  question: string;
  answer: readonly FaqAnswerPart[];
  answerText: string;
}

function answer(...parts: FaqAnswerPart[]): readonly FaqAnswerPart[] {
  return parts;
}

function textFrom(parts: readonly FaqAnswerPart[]) {
  return parts.map((part) => part.text).join("");
}

function item(category: FaqCategory, question: string, parts: readonly FaqAnswerPart[]): FaqItem {
  return {
    category,
    question,
    answer: parts,
    answerText: textFrom(parts),
  };
}

